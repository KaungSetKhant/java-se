package oop_basics;

public class MethhodExample2
{
    static void show()
    {
        System.out.println("Now movie is playing");
    }
    static int result()
    {
        return 4;
    }

    public static void main(String[] args)
    {
        MethhodExample2.show();
        System.out.println(result());
    }
}
